int nmax = 0;
vec<ll> col;
vec<int> puf;
struct plan
{
	ll d,c;
	int x, y;
};
int getp(int v) {
	if (v > nmax) return v;
	if (puf[v] == v) return v;
	return puf[v] = getp(puf[v]);
}
int main(){
    cin.tie(0);
    cout.tie(0);
    ios_base::sync_with_stdio(0);
    int t; cin >> t;
    while (t--) {
    	int n, q;
    	ll k;
    	cin >> n >> k >> q;
    	vec<plan> a(q);
    	for (int i = 0; i < q; i++) {
    		cin >> a[i].d >> a[i].x >> a[i].y >> a[i].c;
    	}
    	if (k > 20) {
    		cout << 0 << endl;
    		// just skip rn
    		continue;
    	}
    	ll lim = 1LL << k;
    	ll ans = 0;
    	nmax = n;
    	col.assign(n + 2, 0);
    	puf.assign(n + 2, 0);
    	vec<int> ord(q);
    	for (ll z = 0; z < lim; z++){ 
    		for (int i = 0; i <= n + 1; i++) {
    			col[i] = 0;
    			puf[i] = i;
    		}
    		for (int i = 0; i < q; i++) ord[i] = i;
    		sort(all(ord), [&](int i, int j) {
    			return (a[i].d ^ z) < (a[j].d ^ z);
    		});
    		for (int it = q - 1; it >= 0; it--) {
    			int id = ord[it];
    			int l = a[id].x;
    			int r = a[id].y;
    			ll c = a[id].c;
    			int pos = getp(l);
    			while (pos <= r) {
    				col[pos] = c % mod;
    				puf[pos] = getp(pos + 1);
    				pos = getp(pos);
    			}
    		}
    		ll cur = 0;
    		for (int i = 1; i <= n; i++) {
    			cur += col[i];
    			if (cur >= mod) cur-= mod;
    		}
    		ans += cur;
    		if (ans >= mod) ans-= mod;
    	}
    	cout << ans % mod << endl;
    }
}
    