bool ok(const vec<pll> &p, ll k) {
	int n = p.size();
	ll best_y = (ll)-4e18;
	int i = 0;
	for (int j = 0; j < n; j++) {
		ll xj = p[j].fi;
		ll yj =p[j].se;
		while (i < j && xj - p[i].fi >= k) {
			if (p[i].se > best_y) best_y = p[i].se;
			i++;
		}
		if (best_y >= yj + k) return 1;
	}
	return 0;
}
